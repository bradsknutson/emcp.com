                            <h3>Essential Question</h3>
                            <p>An essential question anchors student learning, such as:</p>
                            <ul>
                                <li>How does a country's educational system reflect its cultural values? (Kapitel 4)</li>
                                <li>What stories do capital cities tell? (Kapitel 5)</li>
                                <li>How do people in other cultures balance work and leisure time? (Kapitel 9)</li>
                                <li>What kind of learning experiences does travel make possible? (Kapitel 11)</li>
                            </ul>
                            
                            <h3>Interpersonal, Presentational, and Interpretive Communication</h3>
                            <p>Interpersonal Speaking skills are developed in real-life communicative interactions with a partner or group. Interpersonal Writing may involve responding to an e-mail message or a message on social media. Presentational Speaking and Writing activities allow students to share their lives and opinions with classmates and other German-speaking communities online.</p>

                            <p>Interpretive Communication activities ask students to demonstrate their understanding through activities rather than answering questions.</p>

                            <h3>Products, Perspectives, Practices</h3>
                            <p>Students interact with the products, perspectives, and practices of German-speaking cultures while making comparisons to cultural aspects of their own communities. Students investigate and analyze cultural perspectives that underlie the practices in the German-speaking world.</p>

                            <p>Students relate cultural products to understanding the meaning of divergent perspectives within the cultures of the German-speaking world.</p>

                            <h3>Differentiated Learning</h3>
                            <p>Key words in activity models are highlighted in yellow to help slower-paced students. Additional vocabulary and literary selections allows advanced students to proceed at a faster pace.</p>

                            <p>Activities for different learning styles and subject area interests in which students make connections are provided.</p>

                            <h3>Project-based Learning / Assessment</h3>
                            <p>Ideas for project-based learning and assessment are included in the <em>Machen wir weiter!</em> section. There is always one activity that designates the student to work in a group or as a class in order to build communities within the school or online.</p>

